// Helper functions
export const getAllProducts = () => {
  return Object.values(products).flat();
};

export const getProductById = (productId) => {
  return getAllProducts().find(product => product.id === productId);
};

export const getProductsByCategory = (category) => {
  return products[category] || [];
};

export const getProductsByTag = (tag) => {
  return getAllProducts().filter(product => 
    product.tags.some(productTag => 
      productTag.toLowerCase().includes(tag.toLowerCase())
    )
  );
};

export const getProductsByPriceRange = (minPrice, maxPrice) => {
  return getAllProducts().filter(product => {
    const price = parseInt(product.price.replace('₹', ''));
    return price >= minPrice && price <= maxPrice;
  });
};

export const getHighProteinProducts = (minProtein = 20) => {
  return getAllProducts().filter(product => {
    const protein = parseFloat(product.protein.replace('g', ''));
    return protein >= minProtein;
  });
};

export const getProductsByGoal = (goal) => {
  const goalMap = {
    'weight-loss': ['WEIGHT LOSS'],
    'weight-gain': ['WEIGHT GAIN'],
    'muscle-building': ['MUSCLE BUILDING', 'MUSCLE BUILDER'],
    'energy': ['HIGH ENERGY', 'ENERGY BOOST'],
    'recovery': ['RECOVERY', 'POST-WORKOUT'],
    'hydration': ['HYDRATING', 'HYDRATION']
  };
  
  const relevantTags = goalMap[goal] || [];
  return getAllProducts().filter(product =>
    product.tags.some(tag => 
      relevantTags.some(goalTag => 
        tag.toLowerCase().includes(goalTag.toLowerCase())
      )
    )
  );
};

// Nutrition analysis helpers
export const getNutritionSummary = (productId) => {
  const product = getProductById(productId);
  if (!product) return null;
  
  const nutrition = product.nutrition;
  return {
    calories: nutrition['Calories'],
    protein: nutrition['Protein'],
    carbs: nutrition['Carbohydrates'],
    fat: nutrition['Total Fat'],
    fiber: nutrition['Dietary Fiber'],
    keyMinerals: {
      calcium: nutrition['Calcium'],
      iron: nutrition['Iron'],
      magnesium: nutrition['Magnesium']
    }
  };
};

export const compareProducts = (productId1, productId2) => {
  const product1 = getProductById(productId1);
  const product2 = getProductById(productId2);
  
  if (!product1 || !product2) return null;
  
  return {
    product1: {
      name: product1.name,
      price: product1.price,
      protein: product1.protein,
      calories: product1.nutrition['Calories']
    },
    product2: {
      name: product2.name,
      price: product2.price,
      protein: product2.protein,
      calories: product2.nutrition['Calories']
    }
  };
};